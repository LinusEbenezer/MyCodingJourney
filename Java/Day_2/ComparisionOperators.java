package MyCodingJourney.Java.Day_2;

public class ComparisionOperators {
    public static void main(String[] args) {
        int a = 5, b = 3;

        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b);  // true
        System.out.println(a < b);  // false
        System.out.println(a >= b); // true
        System.out.println(a <= b); // false
    }
}

