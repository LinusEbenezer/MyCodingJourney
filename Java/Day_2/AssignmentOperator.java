package MyCodingJourney.Java.Day_2;

public class AssignmentOperator {
    public static void main(String[] args) {
        int num = 10;

        num += 5; // num = 15
        num -= 3; // num = 12
        num *= 2; // num = 24
        num /= 4; // num = 6
        num %= 4; // num = 2

        System.out.println("Final num: " + num);
    }
}


